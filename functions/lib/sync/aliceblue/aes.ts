// Minimal, dependency-free AES-ECB encrypt (128/192/256-bit keys). Web Crypto's
// SubtleCrypto deliberately does not expose ECB mode, but Alice Blue's ANT API
// login flow requires it for the password-encryption step — hence this small
// self-contained implementation rather than pulling in a crypto library.
// Encrypt-only; no padding beyond PKCS#7, no decrypt (not needed here).

