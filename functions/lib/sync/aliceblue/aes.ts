const RCON = new Uint8Array([0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36, 0x6c, 0xd8, 0xab, 0x4d])

function xtime(a: number): number {
  return ((a << 1) ^ (a & 0x80 ? 0x1b : 0)) & 0xff
}

function keyExpansion(key: Uint8Array): Uint8Array[] {
  const nk = key.length / 4
  const nr = nk + 6
  const w: number[][] = []
  for (let i = 0; i < nk; i++) {
    w.push([key[4 * i], key[4 * i + 1], key[4 * i + 2], key[4 * i + 3]])
  }
  for (let i = nk; i < 4 * (nr + 1); i++) {
    let temp = [...w[i - 1]]
    if (i % nk === 0) {
      temp = [temp[1], temp[2], temp[3], temp[0]].map((b) => SBOX[b])
      temp[0] ^= RCON[i / nk - 1]
    } else if (nk > 6 && i % nk === 4) {
      temp = temp.map((b) => SBOX[b])
    }
    w.push(w[i - nk].map((b, idx) => b ^ temp[idx]))
  }
  const roundKeys: Uint8Array[] = []
  for (let r = 0; r <= nr; r++) {
    const rk = new Uint8Array(16)
    for (let c = 0; c < 4; c++) {
      const word = w[r * 4 + c]
      rk[4 * c] = word[0]
      rk[4 * c + 1] = word[1]
      rk[4 * c + 2] = word[2]
      rk[4 * c + 3] = word[3]
    }
    roundKeys.push(rk)
  }
  return roundKeys
}

function addRoundKey(state: Uint8Array, roundKey: Uint8Array): void {
  for (let i = 0; i < 16; i++) state[i] ^= roundKey[i]
}

function subBytes(state: Uint8Array): void {
  for (let i = 0; i < 16; i++) state[i] = SBOX[state[i]]
}

function shiftRows(state: Uint8Array): void {
  const t = Uint8Array.from(state)
  // state is column-major: state[col*4 + row]
  for (let row = 1; row < 4; row++) {
    for (let col = 0; col < 4; col++) {
      state[col * 4 + row] = t[((col + row) % 4) * 4 + row]
    }
  }
}

function gmul(a: number, b: number): number {
  let p = 0
  let aa = a
  let bb = b
  for (let i = 0; i < 8; i++) {
    if (bb & 1) p ^= aa
    aa = xtime(aa)
    bb >>= 1
  }
  return p & 0xff
}

function mixColumns(state: Uint8Array): void {
  for (let c = 0; c < 4; c++) {
    const s0 = state[c * 4]
    const s1 = state[c * 4 + 1]
    const s2 = state[c * 4 + 2]
    const s3 = state[c * 4 + 3]
    state[c * 4] = gmul(s0, 2) ^ gmul(s1, 3) ^ s2 ^ s3
    state[c * 4 + 1] = s0 ^ gmul(s1, 2) ^ gmul(s2, 3) ^ s3
    state[c * 4 + 2] = s0 ^ s1 ^ gmul(s2, 2) ^ gmul(s3, 3)
    state[c * 4 + 3] = gmul(s0, 3) ^ s1 ^ s2 ^ gmul(s3, 2)
  }
}

function encryptBlock(block: Uint8Array, roundKeys: Uint8Array[]): Uint8Array {
  const nr = roundKeys.length - 1
  const state = Uint8Array.from(block)
  addRoundKey(state, roundKeys[0])
  for (let round = 1; round < nr; round++) {
    subBytes(state)
    shiftRows(state)
    mixColumns(state)
    addRoundKey(state, roundKeys[round])
  }
  subBytes(state)
  shiftRows(state)
  addRoundKey(state, roundKeys[nr])
  return state
}

/** PKCS#7-pads `data` and encrypts it with AES-ECB using `key` (16/24/32 bytes). */
export function aesEcbEncrypt(data: Uint8Array, key: Uint8Array): Uint8Array {
  const roundKeys = keyExpansion(key)
  const padLen = 16 - (data.length % 16)
  const padded = new Uint8Array(data.length + padLen)
  padded.set(data)
  padded.fill(padLen, data.length)

  const out = new Uint8Array(padded.length)
  for (let offset = 0; offset < padded.length; offset += 16) {
    const block = encryptBlock(padded.subarray(offset, offset + 16), roundKeys)
    out.set(block, offset)
  }
  return out
}

export function bytesToHex(bytes: Uint8Array): string {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, '0'))
    .join('')
}
